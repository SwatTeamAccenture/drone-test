package br.com.drone.dronetest;

public class DroneDirection
{
    private int _x;
    private int _y;
    public int getX() {
        return _x;
    }
    public void setX(int x) {
        _x = x;
    }
    public int getY() {
        return _y;
    }
    public void setY(int y) {
        _y = y;
    }
    
}